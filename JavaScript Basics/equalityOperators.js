// Strict Equality Operator (Ensures both values on the sides of the operator have the same type and value)
console.log(1 === 1); // This would be true
console.log("1" === 1); // This would be false

// Loose Equality Operator (Ensures both are the same value but it doesn't care if they aren't the same type)
console.log(1 == 1); // This would be true
console.log("1" == 1); // This would be true
console.log(true == 1); // This would be true
